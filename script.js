function randomToken(prefix = "run") {
  runEntropyCounter += 1;

  const highResolutionTime = Math.floor((globalThis.performance?.now?.() || 0) * 1000).toString(36);
  let randomPart = "";

  if (globalThis.crypto?.getRandomValues) {
    const values = new Uint32Array(4);
    globalThis.crypto.getRandomValues(values);
    randomPart = [...values].map((value) => value.toString(36).padStart(7, "0")).join("");
  } else {
    randomPart = Array.from({ length: 4 }, () => Math.floor(Math.random() * 0x100000000).toString(36)).join("");
  }

  return `${prefix}-${Date.now().toString(36)}-${highResolutionTime}-${runEntropyCounter.toString(36)}-${randomPart}`;
}

function hashRunSeed(value) {
  let hash = 2166136261;
  for (const character of String(value)) {
    hash ^= character.charCodeAt(0);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

function createSeededRandom(seed) {
  let state = hashRunSeed(seed) || 1;
  return function seededRandom() {
    state += 0x6D2B79F5;
    let value = state;
    value = Math.imul(value ^ (value >>> 15), value | 1);
    value ^= value + Math.imul(value ^ (value >>> 7), value | 61);
    return ((value ^ (value >>> 14)) >>> 0) / 4294967296;
  };
}

function shuffledCopy(items, random = Math.random) {
  const copy = [...items];
  for (let index = copy.length - 1; index > 0; index--) {
    const randomIndex = Math.floor(random() * (index + 1));
    [copy[index], copy[randomIndex]] = [copy[randomIndex], copy[index]];
  }
  return copy;
}

function readStoredStringArray(key, limit = 100) {
  try {
    const parsed = JSON.parse(localStorage.getItem(key) || "[]");
    if (!Array.isArray(parsed)) return [];
    return parsed
      .filter((value) => typeof value === "string" && value.length > 0)
      .slice(0, limit);
  } catch {
    return [];
  }
}

function writeStoredStringArray(key, values, limit = 100) {
  try {
    localStorage.setItem(key, JSON.stringify(values.slice(0, limit)));
  } catch {
    // Storage can be unavailable in private browsing. The Run still works without history.
  }
}

function runPatternHistoryToken(puzzle) {
  return `${puzzle.concept || puzzle.name}\u001f${puzzle.name}`;
}

function parseRunPatternHistoryToken(token) {
  const separatorIndex = token.indexOf("\u001f");
  if (separatorIndex < 0) return { concept: token, name: token };
  return {
    concept: token.slice(0, separatorIndex),
    name: token.slice(separatorIndex + 1)
  };
}

function rememberRunPattern(puzzle) {
  if (!puzzle?.name) return;

  const token = runPatternHistoryToken(puzzle);
  const history = readStoredStringArray(RUN_RECENT_PATTERN_STORAGE_KEY, RUN_RECENT_PATTERN_LIMIT);
  const nextHistory = [token, ...history.filter((entry) => entry !== token)];
  writeStoredStringArray(RUN_RECENT_PATTERN_STORAGE_KEY, nextHistory, RUN_RECENT_PATTERN_LIMIT);
}

function runGridSizeForRound(roundIndex) {
  let remaining = Math.max(0, roundIndex);

  for (const stage of RUN_GRID_STAGES) {
    if (remaining < stage.rounds) return stage.size;
    remaining -= stage.rounds;
  }

  return RUN_GRID_STAGES[RUN_GRID_STAGES.length - 1].size;
}

function runPreviewTimeForRound(roundIndex) {
  if (roundIndex < RUN_STEADY_PREVIEW_ROUNDS) return RUN_PREVIEW_START_MS;

  const pressureRound = roundIndex - RUN_STEADY_PREVIEW_ROUNDS;
  const pressureSteps = Math.floor(pressureRound / RUN_PREVIEW_STEP_ROUNDS) + 1;
  return Math.max(
    RUN_PREVIEW_MIN_MS,
    RUN_PREVIEW_START_MS - pressureSteps * RUN_PREVIEW_STEP_MS
  );
}

function createRunSequence(seed = runSeed, startRound = 0) {
  const random = createSeededRandom(seed || randomToken("seed"));
  const patternsBySize = new Map();
  const queuesBySize = new Map();
  const recentTokens = readStoredStringArray(RUN_RECENT_PATTERN_STORAGE_KEY, RUN_RECENT_PATTERN_LIMIT);
  const recentNames = new Map();
  const recentConcepts = new Map();

  recentTokens.forEach((token, index) => {
    const { name, concept } = parseRunPatternHistoryToken(token);
    if (!recentNames.has(name)) recentNames.set(name, index);
    if (!recentConcepts.has(concept)) recentConcepts.set(concept, index);
  });

  runPuzzles.forEach((puzzle) => {
    if (!patternsBySize.has(puzzle.size)) patternsBySize.set(puzzle.size, []);
    patternsBySize.get(puzzle.size).push(puzzle);
  });

  function recencyPenalty(puzzle) {
    const nameRank = recentNames.get(puzzle.name);
    const conceptRank = recentConcepts.get(puzzle.concept || puzzle.name);
    let penalty = 0;

    if (nameRank !== undefined) {
      penalty += 5.5 * (1 - Math.min(nameRank, RUN_RECENT_PATTERN_LIMIT) / RUN_RECENT_PATTERN_LIMIT);
    }
    if (conceptRank !== undefined) {
      penalty += 2.5 * (1 - Math.min(conceptRank, RUN_RECENT_PATTERN_LIMIT) / RUN_RECENT_PATTERN_LIMIT);
    }

    return penalty;
  }

  function refillQueue(size, previousPuzzle = null) {
    const source = patternsBySize.get(size) || [];
    if (source.length === 0) {
      throw new Error(`No Run patterns available for ${size}×${size}.`);
    }

    const queue = source
      .map((puzzle) => ({
        puzzle,
        order: random() + recencyPenalty(puzzle)
      }))
      .sort((left, right) => left.order - right.order)
      .map((entry) => entry.puzzle);

    if (queue.length > 1 && previousPuzzle) {
      const betterFirstIndex = queue.findIndex((puzzle) =>
        puzzle.name !== previousPuzzle.name &&
        puzzle.concept !== previousPuzzle.concept &&
        puzzle.category !== previousPuzzle.category
      );
      if (betterFirstIndex > 0) {
        [queue[0], queue[betterFirstIndex]] = [queue[betterFirstIndex], queue[0]];
      }
    }

    queuesBySize.set(size, queue);
  }

  function drawPattern(size, previousPuzzle = null, usedConcepts = new Set()) {
    let queue = queuesBySize.get(size);
    if (!queue || queue.length === 0) {
      refillQueue(size, previousPuzzle);
      queue = queuesBySize.get(size);
    }

    const candidateCount = Math.min(queue.length, 12);
    let bestIndex = 0;
    let bestScore = Number.POSITIVE_INFINITY;

    for (let index = 0; index < candidateCount; index++) {
      const puzzle = queue[index];
      let score = index * 0.035 + random() * 0.45 + recencyPenalty(puzzle);

      if (previousPuzzle?.name === puzzle.name) score += 20;
      if (previousPuzzle?.concept === puzzle.concept) score += 12;
      if (previousPuzzle?.category === puzzle.category) score += 2.2;
      if (usedConcepts.has(puzzle.concept || puzzle.name)) score += 16;

      if (score < bestScore) {
        bestScore = score;
        bestIndex = index;
      }
    }

    return queue.splice(bestIndex, 1)[0];
  }

  const sequence = [];
  let localIndex = 0;
  let previousPuzzle = null;

  while (localIndex < RUN_SEQUENCE_LENGTH) {
    const absoluteRound = startRound + localIndex;
    const size = runGridSizeForRound(absoluteRound);

    let blockLength = 1;
    while (localIndex + blockLength < RUN_SEQUENCE_LENGTH) {
      const nextAbsoluteRound = startRound + localIndex + blockLength;
      if (runGridSizeForRound(nextAbsoluteRound) !== size) break;
      if (absoluteRound >= RUN_STEADY_PREVIEW_ROUNDS && blockLength >= RUN_PREVIEW_STEP_ROUNDS) break;
      blockLength += 1;
    }

    const block = [];
    const usedConcepts = new Set();
    let drawPrevious = previousPuzzle;

    for (let offset = 0; offset < blockLength; offset++) {
      const puzzle = drawPattern(size, drawPrevious, usedConcepts);
      block.push(puzzle);
      usedConcepts.add(puzzle.concept || puzzle.name);
      drawPrevious = puzzle;
    }

    // Selection is random; only the small block is softly ordered by visual complexity.
    // Grid size and preview time remain the main difficulty progression.
    const orderedBlock = block
      .map((puzzle) => ({
        puzzle,
        order: estimateRunPatternDifficulty(puzzle) + (random() - 0.5) * 2.4
      }))
      .sort((left, right) => left.order - right.order)
      .map((entry) => entry.puzzle);

    for (let index = 1; index < orderedBlock.length; index++) {
      if (orderedBlock[index - 1].category !== orderedBlock[index].category) continue;
      const swapIndex = orderedBlock.findIndex((puzzle, candidateIndex) =>
        candidateIndex > index && puzzle.category !== orderedBlock[index - 1].category
      );
      if (swapIndex > index) {
        [orderedBlock[index], orderedBlock[swapIndex]] = [orderedBlock[swapIndex], orderedBlock[index]];
      }
    }

    orderedBlock.forEach((puzzle, offset) => {
      const roundIndex = startRound + localIndex + offset;
      sequence.push({
        ...puzzle,
        time: runPreviewTimeForRound(roundIndex)
      });
    });

    previousPuzzle = orderedBlock[orderedBlock.length - 1] || previousPuzzle;
    localIndex += blockLength;
  }

  return sequence;
}

function ensureRunSequence() {
  if (runSequence.length === 0) {
    runSequence = createRunSequence(`${runSeed}:${runSequenceCycle}`, 0);
  }

  if (runSequenceIndex >= runSequence.length) {
    const previousPuzzle = runSequence[runSequence.length - 1];
    runSequenceCycle += 1;
    const startRound = runSequenceCycle * RUN_SEQUENCE_LENGTH;
    runSequence = createRunSequence(`${runSeed}:${runSequenceCycle}`, startRound);

    if (runSequence.length > 1 && previousPuzzle) {
      const betterFirstIndex = runSequence.findIndex((puzzle) =>
        puzzle.name !== previousPuzzle.name &&
        puzzle.concept !== previousPuzzle.concept &&
        puzzle.category !== previousPuzzle.category
      );
      if (betterFirstIndex > 0) {
        [runSequence[0], runSequence[betterFirstIndex]] = [runSequence[betterFirstIndex], runSequence[0]];
      }
    }

    runSequenceIndex = 0;
  }
}

function beginRun() {
  cancelRunSubmitAutoOpen();
  runBestAtRunStart = runBest;
  runId = randomToken("run");

  const previousPrefix = readStoredStringArray(RUN_LAST_PREFIX_STORAGE_KEY, RUN_PREFIX_LENGTH);
  let nextSeed = "";
  let nextSequence = [];
  let nextPrefix = [];

  for (let attempt = 0; attempt < 8; attempt++) {
    nextSeed = randomToken("seed");
    nextSequence = createRunSequence(`${nextSeed}:0`, 0);
    nextPrefix = nextSequence.slice(0, RUN_PREFIX_LENGTH).map((puzzle) => puzzle.name);

    const samePositions = nextPrefix.reduce(
      (total, name, index) => total + (name === previousPrefix[index] ? 1 : 0),
      0
    );
    const sufficientlyDifferent = nextPrefix[0] !== previousPrefix[0] && samePositions <= 1;

    if (previousPrefix.length === 0 || sufficientlyDifferent || attempt === 7) break;
  }

  runSeed = nextSeed;
  runSequenceCycle = 0;
  runSequence = nextSequence;
  runSequenceIndex = 0;
  writeStoredStringArray(RUN_LAST_PREFIX_STORAGE_KEY, nextPrefix, RUN_PREFIX_LENGTH);

  runPassed = 0;
  runActive = true;
  runFinished = false;
  lastCompletedRun = null;
  hideRunSubmitModal();
  runSubmitStatus.textContent = "";
  viewRunLeaderboardBtn.hidden = true;
  submitRunScoreBtn.disabled = false;
  submitRunScoreBtn.textContent = "Submit run";
  updateRunPanel();

  trackEvent("run_started", {
    run_id: runId,
    pattern_pool_size: runPuzzles.length,
    sequence_size: runSequence.length
  });
}

