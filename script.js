function currentPuzzle() {
  if (mode === "daily") {
    return dailyPuzzles[dailyInfo().puzzleIndex];
  }

  if (mode === "custom") {
    if (isCustomBuilder()) {
      return {
        name: "Your Pattern",
        size: customBuilderSize,
        time: CUSTOM_PREVIEW_TIME,
        cells: []
      };
    }
    return customChallengePuzzle;
  }

  if (mode === "run") {
    // Do not choose or reveal a Run pattern before the player starts the run.
    // The neutral 5×5 placeholder keeps the layout stable without leaking a name.
    if (!runActive) {
      return {
        name: "",
        size: RUN_GRID_STAGES[0].size,
        time: RUN_PREVIEW_START_MS,
        cells: []
      };
    }

    ensureRunSequence();
    return runSequence[runSequenceIndex];
  }

  return dailyPuzzles[dailyInfo().puzzleIndex];
}

