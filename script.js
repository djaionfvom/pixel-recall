function applyStrokeCell(i, cell) {
  if (!canDraw || visitedStrokeCells.has(i)) return;

  visitedStrokeCells.add(i);

  if (strokeMode === "paint") {
    if (!selected.has(i)) {
      selected.add(i);
      cell.classList.add("selected");
    }
  } else if (strokeMode === "erase") {
    if (selected.has(i)) {
      selected.delete(i);
      cell.classList.remove("selected");
    }
  }
}

