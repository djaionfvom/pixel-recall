const UI_SCALE_KEY = "pixelRecallUiScale";
const UI_SCALE_MIN = 0.65;
const UI_SCALE_MAX = 1.20;
const UI_SCALE_STEP = 0.10;
let uiScale = Number(localStorage.getItem(UI_SCALE_KEY)) || 1;

function clampUiScale(value) {
  return Math.min(UI_SCALE_MAX, Math.max(UI_SCALE_MIN, Math.round(value * 100) / 100));
}

function applyUiScale(nextScale, persist = true) {
  uiScale = clampUiScale(nextScale);
  document.documentElement.style.setProperty("--ui-scale", String(uiScale));
  scaleResetBtn.textContent = `${Math.round(uiScale * 100)}%`;
  scaleDownBtn.disabled = uiScale <= UI_SCALE_MIN;
  scaleUpBtn.disabled = uiScale >= UI_SCALE_MAX;

  if (persist) localStorage.setItem(UI_SCALE_KEY, String(uiScale));

  requestAnimationFrame(() => sizeGrid(currentPuzzle()));
}

