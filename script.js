function estimateRunPatternDifficulty(puzzle) {
  const occupied = new Set(puzzle.cells);
  const density = puzzle.cells.length / Math.max(1, puzzle.size * puzzle.size);
  let edgeTransitions = 0;
  let horizontalMismatch = 0;
  let verticalMismatch = 0;

  for (let row = 0; row < puzzle.size; row++) {
    for (let column = 0; column < puzzle.size; column++) {
      const index = row * puzzle.size + column;
      const filled = occupied.has(index);
      if (column + 1 < puzzle.size && filled !== occupied.has(index + 1)) edgeTransitions++;
      if (row + 1 < puzzle.size && filled !== occupied.has(index + puzzle.size)) edgeTransitions++;

      const mirroredColumn = puzzle.size - 1 - column;
      const mirroredRow = puzzle.size - 1 - row;
      if (column < mirroredColumn && filled !== occupied.has(row * puzzle.size + mirroredColumn)) horizontalMismatch++;
      if (row < mirroredRow && filled !== occupied.has(mirroredRow * puzzle.size + column)) verticalMismatch++;
    }
  }

  const maximumEdges = Math.max(1, 2 * puzzle.size * (puzzle.size - 1));
  const boundaryComplexity = edgeTransitions / maximumEdges;
  const symmetryPairs = Math.max(1, puzzle.size * Math.floor(puzzle.size / 2));
  const asymmetry = (horizontalMismatch + verticalMismatch) / (symmetryPairs * 2);
  const densityComplexity = 1 - Math.min(1, Math.abs(density - 0.34) / 0.34);

  // Grid size is the main progression. The rest only orders patterns inside one size tier.
  return (puzzle.size * 100) + (boundaryComplexity * 28) + (asymmetry * 12) + (densityComplexity * 6);
}

