function buildGrid() {
  clearTimeout(previewTimer);
  cancelRunSubmitAutoOpen();
  const puzzle = currentPuzzle();

  selected.clear();
  grid.classList.remove("drawing-active");
  canDraw = false;
  isDrawing = false;
  activePointerId = null;
  visitedStrokeCells.clear();
  lastResult = null;
  preparedShare = null;
  sharePreparationPromise = null;
  hideSharePanel();
  hideDailyResultsModal();
  hideRunLeaderboardModal();
  hideRunSubmitModal();
  if (dailyProgressPanel) dailyProgressPanel.hidden = true;
  updateRunPanel();
  scoreBox.textContent = "—";
  puzzleName.textContent = puzzle.name;
  puzzleName.hidden = mode === "daily" || (mode === "run" && !runActive);
  levelInfo.textContent = currentRoundLabel(puzzle);
  customSizeControl.hidden = !isCustomBuilder();
  customSizeSelect.value = String(customBuilderSize);
  grid.innerHTML = "";
  sizeGrid(puzzle);

  for (let i = 0; i < puzzle.size * puzzle.size; i++) {
    const cell = document.createElement("div");
    cell.className = "cell";
    cell.dataset.index = i;
    cell.setAttribute("role", "button");
    cell.setAttribute("aria-label", `Square ${i + 1}`);

    cell.addEventListener("pointerdown", (event) => {
      if (!canDraw) return;

      // Leave secondary touch pointers to the browser so two-finger pinch zoom works.
      if (event.pointerType === "touch" && !event.isPrimary) {
        isDrawing = false;
        activePointerId = null;
        visitedStrokeCells.clear();
        strokeMode = null;
        return;
      }

      if (event.pointerType !== "touch") event.preventDefault();
      isDrawing = true;
      activePointerId = event.pointerId;
      visitedStrokeCells.clear();

      strokeMode = selected.has(i) ? "erase" : "paint";

      if (event.pointerType !== "touch") {
        cell.setPointerCapture?.(event.pointerId);
      }
      applyStrokeCell(i, cell);
    });

    grid.appendChild(cell);
  }

  resetButtons();

  if (isCustomBuilder()) {
    canDraw = true;
    grid.classList.add("drawing-active");
    startBtn.textContent = "Create challenge link";
    clearBtn.disabled = false;
    checkBtn.hidden = true;
    puzzleName.hidden = false;
    message.textContent = `Draw a ${customBuilderSize}×${customBuilderSize} pattern for your friend, then create a challenge link.`;
    return;
  }

  if (mode === "custom") {
    message.textContent = "Press Start to memorize your friend's pattern.";
  } else if (mode === "run") {
    startBtn.textContent = runActive ? "Next Pattern" : "Start Run";
    message.textContent = runActive
      ? "Press Start and memorize the pattern."
      : `Pass each pattern with at least ${PASS_SCORE}% accuracy. One miss ends the run.`;
  } else {
    message.textContent = "Press Start and memorize the pattern.";
  }
}

